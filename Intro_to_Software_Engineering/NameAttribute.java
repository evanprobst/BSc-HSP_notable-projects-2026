    /** 
     * Updates the attributes on the screen based on the type of pet
     */
    private void updateAttributeLabels() {
        switch (petType) {
            /** Show attributes of the pet based on pet type (different starting healths and rates of decline) */
            case 1: // DOG
                jLabel5.setText("Health: 150");
                jLabel6.setText("Sleep: Increases Normally");
                jLabel7.setText("Happiness: Gains More Happiness");
                jLabel8.setText("Fullness: Decreases Faster");
                break;

            case 2: // CAT
                jLabel5.setText("Health: 100");
                jLabel6.setText("Sleep: Gains More Sleep");
                jLabel7.setText("Happiness: Decreases Faster");
                jLabel8.setText("Fullness: Decreases Normally");
                break;

            case 3: // FISH
                jLabel5.setText("Health: 75");
                jLabel6.setText("Sleep: Decreases More");
                jLabel7.setText("Happiness: Decreases Normally");
                jLabel8.setText("Fullness: Gains More Fullness");
                break;

            default:
                jLabel5.setText("Health: Unknown");
                jLabel6.setText("Sleep: Unknown");
                jLabel7.setText("Happiness: Unknown");
                jLabel8.setText("Fullness: Unknown");
                break;
        }
        String imagePath = "";
        /** Image to be shown on the screen */
        switch (petType) {
            case 1: // DOG
                imagePath = "/img/titleDog.png";
                break;
            case 2: // CAT
                imagePath = "/img/titleCat.png";
                break;
            case 3: // FISH
                imagePath = "/img/titleFish.png";
                break;
        }

if (!imagePath.isEmpty()) {
    petImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource(imagePath)));
}

    }
