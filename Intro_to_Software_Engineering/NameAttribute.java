    /**
     * Method to update the CSV row for the given save slot
     * @param petName The user's chosen name for the pet.
     *  */ 
    private void updateGameData(String petName) {
        String filePath = "savefile.csv";
        File file = new File(filePath);
        List<String> lines = new ArrayList<>();
        /** Read each line from the csv file */
        try (Scanner scanner = new Scanner(file)) {
            while (scanner.hasNextLine()) {
                lines.add(scanner.nextLine());
            }
        } catch (IOException e) {
            e.printStackTrace();
            return;
        }
        /** Retrieve the pet type stored in the save file */
        String oldLine = lines.get(saveSlot);
        String petType = "";
        if (oldLine != null && !oldLine.trim().isEmpty()) {
            String[] parts = oldLine.split(",");
            if (parts.length > 0) {
                petType = parts[0].trim();
            }
        }
        String health = "100";
        switch (petType) {
            /** Dog */
            case "1": 
            health = "150";
                break;
            /**  Cat */
            case "2": 
            health = "100";
                break;
                /** Fish */
            case "3": 
            health = "75";
                break;
        } 
        /** build the new row in the correct file format, with initial values */
        String newRow = petType + "," + "5" + "," + petName + "," + health + ",100,100,100,0,10,5,1,10,5,1";
        lines.set(saveSlot, newRow);

        /** Write the updated lines back to the file */
        try (PrintWriter writer = new PrintWriter(file)) {
            for (String line : lines) {
                writer.println(line);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
